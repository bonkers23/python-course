# This program demonstrates different data types in Python

number = 10              # Integer
decimal = 5.5            # Float
text = "Python"          # String
is_learning = True       # Boolean

print("Number:", number)
print("Decimal:", decimal)
print("Text:", text)
print("Is Learning:", is_learning)

print("Data type of number:", type(number))
print("Data type of decimal:", type(decimal))
print("Data type of text:", type(text))
print("Data type of is_learning:", type(is_learning))

