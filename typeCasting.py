# M1L3 Activity 2
# Topic: Typecasting
# This program demonstrates typecasting in Python

# String to Integer
num_str = "10"
num_int = int(num_str)

# Integer to Float
num_float = float(num_int)

# Integer to String
num_text = str(num_int)

print("Original value:", num_str, "Type:", type(num_str))
print("After int conversion:", num_int, "Type:", type(num_int))
print("After float conversion:", num_float, "Type:", type(num_float))
print("After string conversion:", num_text, "Type:", type(num_text))

# End of program
